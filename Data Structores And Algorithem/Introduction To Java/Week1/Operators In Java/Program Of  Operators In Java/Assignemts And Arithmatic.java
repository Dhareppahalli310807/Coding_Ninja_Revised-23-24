class Operators{
    public static void main(String args[]){
        // 1. assignments operator(=)
        int i = 10;
        int j = i;
        System.out.println(i);
        System.out.println(j);

        // 2. Arithmatic operator(+,-,*,/)
        System.out.println(100 + 20);
        System.out.println(100 - 20);
        System.out.println(100 * 20);
        System.out.println(100 / 20);
        System.out.println(100 % 20);

        // 3. unary operator(+, -, --, ++)
        int a = 1;
        System.out.println(a);
        System.out.println(a++);
    }
        
}